def main() -> None:
    questions = json.loads(QUESTIONS_PATH.read_text(encoding="utf-8"))
    legacy_questions = [item for item in questions if item["id"] in LEGACY_EXPECTATIONS]
    if len(legacy_questions) != 24:
        raise RuntimeError(f"expected 24 legacy questions, got {len(legacy_questions)}")

    for item in legacy_questions:
        risk, recommend, ready, insufficient, notes = LEGACY_EXPECTATIONS[item["id"]]
        item["type"] = item.get("type") or item["test_type"]
        item["expected_doc"] = item.get("expected_doc") or item["expected_documents"][0]
        item["expected_risk_level"] = risk
        item["expected_recommend_workorder"] = recommend
        item["expected_ready_to_create"] = ready
        item["expected_insufficient_basis"] = insufficient
        item["notes"] = notes
        if item["id"] in RECOMMEND_OVERRIDES:
            item["expected_recommend_workorder"] = RECOMMEND_OVERRIDES[item["id"]]
        if item["id"] in READY_OVERRIDES:
            item["expected_ready_to_create"] = READY_OVERRIDES[item["id"]]
        if item["id"] in RISK_LEVEL_SETS:
            item["expected_risk_level_set"] = RISK_LEVEL_SETS[item["id"]]

    output = legacy_questions + NEW_QUESTIONS
    for item in output:
        if item["id"] in RISK_LEVEL_SETS:
            item["expected_risk_level_set"] = RISK_LEVEL_SETS[item["id"]]
    ids = [item["id"] for item in output]
    if len(output) != 60:
        raise RuntimeError(f"expected 60 questions, got {len(output)}")
    if len(ids) != len(set(ids)):
        raise RuntimeError("duplicate question id found")

    QUESTIONS_PATH.write_text(json.dumps(output, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"wrote {len(output)} questions to {QUESTIONS_PATH}")


if __name__ == "__main__":
    main()
