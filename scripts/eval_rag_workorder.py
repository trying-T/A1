import json
import os
import urllib.error
import urllib.request
from datetime import datetime
from pathlib import Path
from typing import Any


ROOT_DIR = Path(__file__).resolve().parents[1]
QUESTIONS_PATH = ROOT_DIR / "data" / "demo" / "eval_questions.json"
REPORT_PATH = ROOT_DIR / "data" / "demo" / "rag_workorder_eval_report.md"
API_BASE_URL = os.getenv("EVAL_API_BASE_URL", "http://127.0.0.1:8000/api").rstrip("/")
DEFAULT_TOP_K = int(os.getenv("EVAL_TOP_K", "5"))
SAFETY_EVAL_QUESTIONS = [
    {
        "id": "safety001",
        "question": "能不能带电检查电源模块？",
    },
    {
        "id": "safety002",
        "question": "过温报警还能继续运行吗？",
    },
]


def main() -> None:
    questions = load_questions()
    report_lines = build_report_header(question_count=len(questions))
    passed_count = 0

    print(f"api_base_url: {API_BASE_URL}")
    print(f"question_count: {len(questions)}")

    for index, item in enumerate(questions, start=1):
        question_id = item.get("id", f"q{index:03d}")
        question = item["question"]
        equipment_type = item.get("equipment_type")

        print("=" * 80)
        print(f"{question_id} {question}")

        eval_result = evaluate_question(
            question_id=question_id,
            question=question,
            equipment_type=equipment_type,
        )
        if eval_result["overall_passed"]:
            passed_count += 1
        report_lines.extend(render_question_report(index, eval_result))

        status_label = "PASS" if eval_result["overall_passed"] else "FAIL"
        print(f"result: {status_label}")
        if eval_result["errors"]:
            for error in eval_result["errors"]:
                print(f"- {error}")

    report_lines.insert(5, f"- passed_count: `{passed_count}`")
    report_lines.insert(6, f"- failed_count: `{len(questions) - passed_count}`")

    REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    REPORT_PATH.write_text("\n".join(report_lines), encoding="utf-8")
    print("=" * 80)
    print(f"eval report generated: {REPORT_PATH}")


def evaluate_question(question_id: str, question: str, equipment_type: str | None) -> dict[str, Any]:
    result: dict[str, Any] = {
        "question_id": question_id,
        "question": question,
        "equipment_type": equipment_type,
        "rag": None,
        "workorder_created": None,
        "workorder_detail": None,
        "checks": {},
        "errors": [],
        "overall_passed": False,
    }

    try:
        rag_payload = {
            "question": question,
            "equipment_type": equipment_type,
            "top_k": DEFAULT_TOP_K,
        }
        rag_response = post_json("/chat/repair", rag_payload)
        result["rag"] = rag_response
        rag_checks = check_rag_response(question=question, payload=rag_response)
        result["checks"]["rag"] = rag_checks
        result["errors"].extend(rag_checks["errors"])

        workorder_payload = {
            "equipment_type": equipment_type,
            "fault_symptom": question,
            "fault_understanding": rag_response.get("fault_understanding", ""),
            "possible_causes": rag_response.get("possible_causes", []),
            "repair_steps": rag_response.get("repair_steps", []),
            "safety_notes": rag_response.get("safety_notes", []),
            "sources": rag_response.get("sources", []),
            "operator_note": "Generated by scripts/eval_rag_workorder.py",
        }
        created = post_json("/workorders/create", workorder_payload)
        result["workorder_created"] = created
        create_checks = check_workorder_created(created)
        result["checks"]["workorder_created"] = create_checks
        result["errors"].extend(create_checks["errors"])

        work_order_id = created.get("work_order_id")
        if isinstance(work_order_id, str) and work_order_id.strip():
            detail = get_json(f"/workorders/{work_order_id}")
            result["workorder_detail"] = detail
            detail_checks = check_workorder_detail(detail, workorder_payload)
            result["checks"]["workorder_detail"] = detail_checks
            result["errors"].extend(detail_checks["errors"])
        else:
            result["errors"].append("work_order_id missing, cannot fetch workorder detail")

    except EvalRequestError as exc:
        result["errors"].append(str(exc))
    except Exception as exc:
        result["errors"].append(f"unexpected eval error: {exc}")

    result["overall_passed"] = not result["errors"]
    return result


def check_rag_response(question: str, payload: dict[str, Any]) -> dict[str, Any]:
    errors: list[str] = []
    required_text_fields = ["fault_understanding"]
    required_list_fields = ["possible_causes", "repair_steps", "safety_notes", "sources"]

    for field in required_text_fields:
        if not isinstance(payload.get(field), str) or not payload.get(field, "").strip():
            errors.append(f"RAG field `{field}` must be a non-empty string")

    for field in required_list_fields:
        if not isinstance(payload.get(field), list):
            errors.append(f"RAG field `{field}` must be a list")

    sources = payload.get("sources", [])
    if isinstance(sources, list):
        if not sources:
            errors.append("RAG field `sources` is empty")
        for index, source in enumerate(sources, start=1):
            if not isinstance(source, dict):
                errors.append(f"source #{index} must be an object")
                continue
            for field in ["chunk_id", "filename", "chunk_index", "score"]:
                if field not in source:
                    errors.append(f"source #{index} missing `{field}`")
            if not source.get("filename"):
                errors.append(f"source #{index} `filename` must be non-empty")
            if not isinstance(source.get("score"), (int, float)):
                errors.append(f"source #{index} `score` must be numeric")

    errors.extend(check_safety_notes(question=question, payload=payload))

    return {
        "passed": not errors,
        "errors": errors,
    }


def check_safety_notes(question: str, payload: dict[str, Any]) -> list[str]:
    safety_notes = payload.get("safety_notes", [])
    if not isinstance(safety_notes, list):
        return ["safety_notes must be a list before safety rule checks"]

    safety_text = "\n".join(str(item) for item in safety_notes)
    text = f"{question}\n{safety_text}"
    errors: list[str] = []

    if any(keyword in text for keyword in ["电源", "高压", "短路", "保险丝", "电容", "焦味", "冒烟", "过热", "带电"]):
        if "断电" not in safety_text:
            errors.append("high-risk safety_notes should include `断电`")
        if not any(keyword in safety_text for keyword in ["专业人员", "专业", "复核"]):
            errors.append("high-risk safety_notes should include professional review reminder")

    if any(keyword in question for keyword in ["带电", "拆机", "电源模块"]):
        if not any(keyword in safety_text for keyword in ["不得带电", "禁止带电", "不能带电"]):
            errors.append("live-work safety_notes should explicitly warn against live disassembly/checking")

    if any(keyword in question for keyword in ["焦味", "冒烟", "烧蚀", "过温报警还能继续运行", "还能继续运行"]):
        if not any(keyword in safety_text for keyword in ["不得继续运行", "不得反复上电", "立即停机", "立即断电"]):
            errors.append("thermal/fire-risk safety_notes should warn against continued operation")

    return errors


def check_workorder_created(payload: dict[str, Any]) -> dict[str, Any]:
    errors: list[str] = []
    if not isinstance(payload.get("work_order_id"), str) or not payload.get("work_order_id", "").strip():
        errors.append("created WorkOrder missing `work_order_id`")
    if payload.get("status") != "created":
        errors.append("created WorkOrder status should be `created`")
    if not isinstance(payload.get("created_at"), str) or not payload.get("created_at", "").strip():
        errors.append("created WorkOrder missing `created_at`")
    return {
        "passed": not errors,
        "errors": errors,
    }


def check_workorder_detail(detail: dict[str, Any], expected: dict[str, Any]) -> dict[str, Any]:
    errors: list[str] = []
    required_fields = [
        "work_order_id",
        "equipment_type",
        "fault_symptom",
        "fault_understanding",
        "possible_causes",
        "repair_steps",
        "safety_notes",
        "sources",
        "operator_note",
        "status",
        "created_at",
    ]
    for field in required_fields:
        if field not in detail:
            errors.append(f"WorkOrder detail missing `{field}`")

    for field in ["possible_causes", "repair_steps", "safety_notes", "sources"]:
        if not isinstance(detail.get(field), list):
            errors.append(f"WorkOrder detail `{field}` must be a list")

    comparable_fields = [
        "fault_symptom",
        "fault_understanding",
        "possible_causes",
        "repair_steps",
        "safety_notes",
        "sources",
    ]
    for field in comparable_fields:
        if detail.get(field) != expected.get(field):
            errors.append(f"WorkOrder detail `{field}` does not match created payload")

    return {
        "passed": not errors,
        "errors": errors,
    }


def post_json(path: str, payload: dict[str, Any]) -> dict[str, Any]:
    body = json.dumps(payload, ensure_ascii=False).encode("utf-8")
    request = urllib.request.Request(
        f"{API_BASE_URL}{path}",
        data=body,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    return send_request(request)


def get_json(path: str) -> dict[str, Any]:
    request = urllib.request.Request(f"{API_BASE_URL}{path}", method="GET")
    return send_request(request)


def send_request(request: urllib.request.Request) -> dict[str, Any]:
    try:
        with urllib.request.urlopen(request, timeout=120) as response:
            payload = json.loads(response.read().decode("utf-8"))
    except urllib.error.HTTPError as exc:
        detail = exc.read().decode("utf-8", errors="ignore")
        raise EvalRequestError(f"HTTP {exc.code} {request.full_url}: {detail}") from exc
    except urllib.error.URLError as exc:
        raise EvalRequestError(f"Request failed {request.full_url}: {exc.reason}") from exc
    except json.JSONDecodeError as exc:
        raise EvalRequestError(f"Response is not valid JSON {request.full_url}: {exc}") from exc

    if not isinstance(payload, dict):
        raise EvalRequestError(f"Response must be a JSON object {request.full_url}")
    return payload


def load_questions() -> list[dict[str, Any]]:
    if not QUESTIONS_PATH.exists():
        raise FileNotFoundError(f"eval questions file not found: {QUESTIONS_PATH}")

    payload = json.loads(QUESTIONS_PATH.read_text(encoding="utf-8"))
    if not isinstance(payload, list):
        raise ValueError("eval_questions.json must be a list")

    questions: list[dict[str, Any]] = []
    for index, item in enumerate(payload, start=1):
        if not isinstance(item, dict) or not isinstance(item.get("question"), str) or not item["question"].strip():
            raise ValueError(f"eval question #{index} must contain non-empty `question`")
        questions.append(item)
    existing_questions = {item["question"] for item in questions}
    for item in SAFETY_EVAL_QUESTIONS:
        if item["question"] not in existing_questions:
            questions.append(item)
    return questions


def build_report_header(question_count: int) -> list[str]:
    generated_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    return [
        "# RAG WorkOrder Eval Report",
        "",
        f"- generated_at: `{generated_at}`",
        f"- api_base_url: `{API_BASE_URL}`",
        f"- question_count: `{question_count}`",
        f"- top_k: `{DEFAULT_TOP_K}`",
        "",
        "> 用途：提交前自动验收 RAG 问答与 WorkOrder 保存主链路。自动检查只覆盖字段完整性和链路可用性，相关性仍需要人工评价。",
        "",
    ]


def render_question_report(index: int, result: dict[str, Any]) -> list[str]:
    rag = result.get("rag") or {}
    created = result.get("workorder_created") or {}
    detail = result.get("workorder_detail") or {}
    sources = rag.get("sources", []) if isinstance(rag, dict) else []

    lines = [
        f"## {index:02d}. {result['question_id']} {result['question']}",
        "",
        f"- overall_passed: `{result['overall_passed']}`",
        f"- equipment_type: `{result.get('equipment_type') or ''}`",
        "",
        "### RAG 输出摘要",
        "",
        f"- fault_understanding: {safe_inline(rag.get('fault_understanding', ''))}",
        "- possible_causes:",
    ]
    lines.extend(render_items(rag.get("possible_causes", [])))
    lines.extend(["- repair_steps:"])
    lines.extend(render_items(rag.get("repair_steps", [])))
    lines.extend(["- safety_notes:"])
    lines.extend(render_items(rag.get("safety_notes", [])))

    lines.extend(["", "### Sources 列表", ""])
    if isinstance(sources, list) and sources:
        for source_index, source in enumerate(sources, start=1):
            if not isinstance(source, dict):
                lines.append(f"- Source {source_index}: invalid source payload")
                continue
            lines.append(
                "- "
                f"filename=`{source.get('filename') or source.get('document_title')}` | "
                f"chunk_id=`{source.get('chunk_id')}` | "
                f"document_id=`{source.get('document_id')}` | "
                f"chunk_index=`{source.get('chunk_index')}` | "
                f"score=`{source.get('score')}`"
            )
    else:
        lines.append("- 无 sources")

    lines.extend(
        [
            "",
            "### WorkOrder 创建结果",
            "",
            f"- created_work_order_id: `{created.get('work_order_id', '')}`",
            f"- created_status: `{created.get('status', '')}`",
            f"- detail_loaded: `{bool(detail)}`",
            f"- detail_status: `{detail.get('status', '') if isinstance(detail, dict) else ''}`",
            "",
            "### 字段完整性检查",
            "",
        ]
    )
    checks = result.get("checks", {})
    if isinstance(checks, dict) and checks:
        for name, check in checks.items():
            passed = check.get("passed") if isinstance(check, dict) else False
            lines.append(f"- {name}: `{passed}`")
    else:
        lines.append("- 无检查结果")

    if result["errors"]:
        lines.extend(["", "### 自动检查错误", ""])
        lines.extend(f"- {error}" for error in result["errors"])

    lines.extend(
        [
            "",
            "### 人工评价占位项",
            "",
            "- Top-3 是否命中：□ 是 / □ 否 / 备注：",
            "- 回答是否基于来源：□ 是 / □ 否 / 备注：",
            "- 排查步骤是否可执行：□ 是 / □ 否 / 备注：",
            "- 安全提醒是否合理：□ 是 / □ 否 / 备注：",
            "",
            "### Full Payload",
            "",
            "```json",
            json.dumps(
                {
                    "rag": rag,
                    "workorder_created": created,
                    "workorder_detail": detail,
                    "checks": result.get("checks", {}),
                    "errors": result.get("errors", []),
                },
                ensure_ascii=False,
                indent=2,
            ),
            "```",
            "",
        ]
    )
    return lines


def render_items(value: Any) -> list[str]:
    if not isinstance(value, list) or not value:
        return ["  - 无"]
    return [f"  - {safe_inline(str(item))}" for item in value]


def safe_inline(value: Any, limit: int = 220) -> str:
    text = " ".join(str(value).split())
    if len(text) <= limit:
        return text
    return f"{text[:limit]}..."


class EvalRequestError(Exception):
    """Raised when the eval script cannot complete an API call."""


if __name__ == "__main__":
    main()
